package Calculadora;

public class Calculadora {
	
	public static String getOperacao(String simbolo){
		return simbolo;
	}
	
	public static double getSoma(double valorUm,double valorDois){
		return valorUm+valorDois;
	}
	public static double getSubtracao(double valorUm,double valorDois){
		return valorUm-valorDois;
	}
	public static double getDivisao(double valorUm,double valorDois){
		return valorUm/valorDois;
	}
	public static double getMultiplicacao(double valorUm,double valorDois){
		return valorUm*valorDois;
	}
}
